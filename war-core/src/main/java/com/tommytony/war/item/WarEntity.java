package com.tommytony.war.item;

/**
 * Categories of entities managed by the game.
 */
public enum WarEntity {
    ITEM,           // items on the ground
    PROJECTILE,     // snowballs, fireballs, eggs, etc.
    VEHICLE,        // minecart, boat, etc..
    PROP,           // armor stand, painting, item frame
    TNT,
    FALLING,        // sand, gravel
    MOB,
    PLAYER,
    UNKNOWN

}
